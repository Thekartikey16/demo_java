public class literals {
    public static void main(String[] args) {
         int a = 123; //int
         int b = 0146; //oct
         int c = 0x73b; //hexa
         int d = 0b1101; //binary
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        int sum = a + b + c + d;
        System.out.println("Sum = " + sum);

    }
}
